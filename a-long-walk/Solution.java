    public static int longestHike(String input) {
        char[][] matrix = blockToMatrix(input);

        int startCol = 0;
        int endCol = 0;

        for (int i = 0; i < matrix[0].length; ++i) {
            if (matrix[0][i] == '.') {
                startCol = i;
                break;
            }
        }

        for (int i = 0; i < matrix[matrix.length - 1].length; ++i) {
            if (matrix[matrix.length - 1][i] == '.') {
                endCol = i;
                break;
            }
        }


        return bfs(matrix, 0, startCol, endCol) - 1;
    }

    public static int bfs(char[][] matrix, int r, int c, int endCol) {
        if (r < 0 || c < 0 || r > matrix.length - 1 || c > matrix[r].length - 1) {
            return 0;
        }

        if (matrix[r][c] == '#') {
            return 0;
        }

        if (r == matrix.length - 1 && c == endCol) {
            return 1;
        }

        char temp = matrix[r][c];
        int ans = 0;
        matrix[r][c] = '#';

        if (temp != '.') {
            switch (temp) {
                case '^':
                    ans = bfs(matrix, r - 1, c, endCol) + 1;
                    break;
                case 'v':
                    ans = bfs(matrix, r + 1, c, endCol) + 1;
                    break;
                case '>':
                    ans = bfs(matrix, r, c + 1, endCol) + 1;
                    break;
                case '<':
                    ans = bfs(matrix, r, c - 1, endCol) + 1;
                    break;
                default:
                    break;
            }
        } else {
            int[] xs = {-1, 0, 1, 0};
            int[] ys = {0, - 1, 0, 1};

            for (int i = 0; i < 4; ++i) {
                ans = Math.max(ans, bfs(matrix, r + xs[i], c + ys[i], endCol));
            }
            ++ans;
        }

        matrix[r][c] = temp;

        return ans;
    }

    public static char[][] blockToMatrix(String input) {
        char[] chars = input.toCharArray();

        int width = 0;
        int height = 0;

        for (char c: chars) {
            if (c == '\n') {
                break;
            }
            ++width;
        }

        height = chars.length / (width + 1);

        char[][] matrix = new char[height][width];
        int i = 0;

        for (int r = 0; r < height; ++r) {
            for (int c = 0; c < width; ++c) {
                if (chars[i] == '\n') {
                    ++i;
                }
                matrix[r][c] = chars[i];
                ++i;
            }
        }


        return matrix;
    }


